<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Blog post click functionality
        const blogPosts = document.querySelectorAll('article');
        blogPosts.forEach(post => {
            post.addEventListener('click', function(e) {
                if (!e.target.closest('button') && !e.target.closest('a')) {
                    const title = this.querySelector('h3').textContent;
                    console.log('Opening blog post:', title);
                    // In real app: navigate to blog post detail
                    showNotification(`Opening "${title}"`, 'info');
                }
            });
        });

        // Bookmark buttons
        const bookmarkButtons = document.querySelectorAll('.fa-bookmark');
        bookmarkButtons.forEach(button => {
            button.addEventListener('click', function(e) {
                e.stopPropagation();
                if (this.classList.contains('far')) {
                    this.classList.remove('far', 'text-gray-600');
                    this.classList.add('fas', 'text-orange-500');
                    this.classList.add('animate-pulse');
                    setTimeout(() => {
                        this.classList.remove('animate-pulse');
                    }, 300);
                    showNotification('Article bookmarked!', 'success');
                } else {
                    this.classList.remove('fas', 'text-orange-500');
                    this.classList.add('far', 'text-gray-600');
                    showNotification('Bookmark removed', 'info');
                }
            });
        });

        // Category filter
        const categorySelect = document.querySelector('select');
        if (categorySelect) {
            categorySelect.addEventListener('change', function() {
                const selectedCategory = this.value;
                console.log('Filtering by category:', selectedCategory);

                // In real app: filter blog posts
                showNotification(`Showing ${selectedCategory === 'All Categories' ? 'all' : selectedCategory.toLowerCase()} articles`, 'info');
            });
        }

        // Search functionality
        const searchInput = document.querySelector('input[type="text"]');
        searchInput.addEventListener('keypress', function(e) {
            if (e.key === 'Enter') {
                const searchTerm = this.value.trim();
                if (searchTerm) {
                    console.log('Searching for:', searchTerm);
                    showNotification(`Searching articles for "${searchTerm}"`, 'info');
                }
            }
        });

        // Newsletter form
        const newsletterForm = document.querySelector('form');
        if (newsletterForm) {
            newsletterForm.addEventListener('submit', function(e) {
                e.preventDefault();
                const email = this.querySelector('input[type="email"]').value;

                // Show loading state
                const button = this.querySelector('button');
                const originalText = button.textContent;
                button.innerHTML = '<i class="fas fa-spinner fa-spin mr-2"></i> Subscribing...';
                button.disabled = true;

                setTimeout(() => {
                    // Show success
                    button.innerHTML = '<i class="fas fa-check mr-2"></i> Subscribed!';
                    button.classList.remove('from-orange-500', 'to-yellow-500', 'hover:from-orange-600', 'hover:to-yellow-600');
                    button.classList.add('from-green-500', 'to-green-600');

                    // Reset form
                    this.querySelector('input[type="email"]').value = '';

                    setTimeout(() => {
                        button.innerHTML = originalText;
                        button.classList.remove('from-green-500', 'to-green-600');
                        button.classList.add('from-orange-500', 'to-yellow-500', 'hover:from-orange-600', 'hover:to-yellow-600');
                        button.disabled = false;
                    }, 2000);

                    showNotification('Successfully subscribed to Cooking Insights!', 'success');
                }, 1500);
            });
        }

        // Pagination buttons
        const paginationButtons = document.querySelectorAll('.w-10.h-10');
        paginationButtons.forEach(button => {
            button.addEventListener('click', function() {
                // Remove active state from all buttons
                paginationButtons.forEach(btn => {
                    btn.classList.remove('bg-gradient-to-r', 'from-orange-500', 'to-yellow-500', 'text-white');
                    btn.classList.add('bg-gray-100', 'hover:bg-gray-200');
                });

                // Add active state to clicked button if it's a number
                if (!this.querySelector('i')) {
                    this.classList.remove('bg-gray-100', 'hover:bg-gray-200');
                    this.classList.add('bg-gradient-to-r', 'from-orange-500', 'to-yellow-500', 'text-white');
                }

                console.log('Page changed');
                // In real app: load new articles
            });
        });

        // Social media links
        const socialLinks = document.querySelectorAll('a[href="#"]');
        socialLinks.forEach(link => {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const platform = this.textContent.includes('Instagram') ? 'Instagram' :
                    this.textContent.includes('Facebook') ? 'Facebook' : 'YouTube';
                console.log('Opening', platform);
                showNotification(`Opening ${platform} (demo)`, 'info');
            });
        });

        // Category links
        const categoryLinks = document.querySelectorAll('.space-y-3 a');
        categoryLinks.forEach(link => {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const category = this.querySelector('span:first-child').textContent;
                console.log('Filtering by category:', category);
                showNotification(`Filtering ${category} articles`, 'info');
            });
        });

        // Popular post links
        const popularLinks = document.querySelectorAll('.space-y-4 a');
        popularLinks.forEach(link => {
            link.addEventListener('click', function(e) {
                e.preventDefault();
                const title = this.querySelector('h4').textContent;
                console.log('Opening popular article:', title);
                showNotification(`Opening "${title}"`, 'info');
            });
        });

        // Notification function
        function showNotification(message, type) {
            const notification = document.createElement('div');
            notification.className = `fixed top-6 right-6 px-6 py-4 rounded-xl shadow-xl z-50 transform transition-all duration-500 animate-slide-in-right ${
                type === 'success'
                    ? 'bg-green-500 text-white'
                    : 'bg-orange-500 text-white'
            }`;
            notification.innerHTML = `
                    <div class="flex items-center">
                        <i class="fas fa-${type === 'success' ? 'check-circle' : 'info-circle'} mr-3 text-xl"></i>
                        <span>${message}</span>
                        <button onclick="this.parentElement.parentElement.remove()" class="ml-4">
                            <i class="fas fa-times"></i>
                        </button>
                    </div>
                `;

            document.body.appendChild(notification);

            setTimeout(() => {
                if (notification.parentElement) {
                    notification.remove();
                }
            }, 3000);
        }

        // Animate blog posts on scroll
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -100px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach((entry, index) => {
                if (entry.isIntersecting) {
                    setTimeout(() => {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }, index * 100);
                }
            });
        }, observerOptions);

        // Observe blog posts
        document.querySelectorAll('article').forEach((post, index) => {
            post.style.opacity = '0';
            post.style.transform = 'translateY(20px)';
            post.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
            post.style.transitionDelay = `${index * 0.1}s`;
            observer.observe(post);
        });

        // Featured post click
        const featuredPost = document.querySelector('.bg-white.rounded-2xl.shadow-xl');
        if (featuredPost) {
            featuredPost.addEventListener('click', function(e) {
                if (!e.target.closest('a')) {
                    console.log('Opening featured post');
                    showNotification('Opening featured article: "The Zero-Waste Kitchen Revolution"', 'info');
                }
            });
        }
    });
</script>
