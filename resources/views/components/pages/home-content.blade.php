<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Animate elements on scroll
        function animateOnScroll() {
            const elements = document.querySelectorAll('.animate-fade-in-up, .animate-slide-in-left, .animate-slide-in-right, .animate-float');

            elements.forEach(element => {
                const elementPosition = element.getBoundingClientRect().top;
                const screenPosition = window.innerHeight / 1.2;

                if(elementPosition < screenPosition) {
                    element.style.opacity = '1';
                    element.style.transform = 'translate(0)';
                }
            });
        }

        // Set initial state for animated elements
        document.querySelectorAll('.animate-fade-in-up').forEach(el => {
            el.style.opacity = '0';
            el.style.transform = 'translateY(30px)';
            el.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
        });

        document.querySelectorAll('.animate-slide-in-left').forEach(el => {
            el.style.opacity = '0';
            el.style.transform = 'translateX(-30px)';
            el.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
        });

        document.querySelectorAll('.animate-slide-in-right').forEach(el => {
            el.style.opacity = '0';
            el.style.transform = 'translateX(30px)';
            el.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
        });

        document.querySelectorAll('.animate-float').forEach(el => {
            el.style.opacity = '0';
            el.style.transition = 'opacity 0.8s ease';
        });

        // Initialize
        window.addEventListener('load', animateOnScroll);
        window.addEventListener('scroll', animateOnScroll);

        // Floating food icons animation
        const foodIcons = document.querySelectorAll('.text-4xl, .text-5xl, .text-6xl');
        foodIcons.forEach((icon, index) => {
            icon.style.animationDelay = `${index * 0.5}s`;
        });

        // CTA button interactions
        document.querySelectorAll('button').forEach(button => {
            button.addEventListener('click', function(e) {
                if (this.textContent.includes('Start Free Trial') || this.textContent.includes('Get Started')) {
                    e.preventDefault();

                    // Animation effect
                    this.style.transform = 'scale(0.95)';
                    setTimeout(() => {
                        this.style.transform = '';
                    }, 200);

                    // In a real app, this would trigger signup modal
                    alert('Welcome to MealMate! In a real application, this would open the signup modal.\n\nStart your 14-day free trial now!');
                }
            });
        });

        // Parallax effect for background elements
        window.addEventListener('scroll', function() {
            const scrolled = window.pageYOffset;
            const parallaxElements = document.querySelectorAll('.absolute');

            parallaxElements.forEach(element => {
                if (element.classList.contains('top-20') || element.classList.contains('bottom-20')) {
                    const rate = scrolled * 0.5;
                    element.style.transform = `translateY(${rate}px)`;
                }
            });
        });

        // Count-up animation for stats
        function animateCounters() {
            const counters = document.querySelectorAll('.text-3xl.font-bold');

            counters.forEach(counter => {
                const target = parseInt(counter.textContent.replace('K+', '000').replace('+', '').replace('%', ''));
                if (isNaN(target)) return;

                const increment = target / 100;
                let current = 0;

                const updateCounter = () => {
                    if (current < target) {
                        current += increment;
                        counter.textContent = Math.ceil(current).toLocaleString() +
                            (counter.textContent.includes('K+') ? 'K+' :
                                counter.textContent.includes('%') ? '%' :
                                    counter.textContent.includes('+') ? '+' : '');
                        setTimeout(updateCounter, 20);
                    } else {
                        counter.textContent = target.toLocaleString() +
                            (counter.textContent.includes('K+') ? 'K+' :
                                counter.textContent.includes('%') ? '%' :
                                    counter.textContent.includes('+') ? '+' : '');
                    }
                };

                updateCounter();
            });
        }

        // Trigger counter animation when in view
        const observerOptions = {
            threshold: 0.5
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateCounters();
                }
            });
        }, observerOptions);

        // Observe the stats section
        const statsSection = document.querySelector('.grid.grid-cols-3');
        if (statsSection) {
            observer.observe(statsSection);
        }
    });
</script>
