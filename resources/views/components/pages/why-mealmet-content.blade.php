<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Animate stats on scroll
        function animateStats() {
            const stats = document.querySelectorAll('.text-4xl.font-bold, .text-5xl.font-bold');

            stats.forEach(stat => {
                const value = stat.textContent;
                if (value.includes('%') || value.includes('$') || value.includes('M') || value.includes('K')) {
                    const numValue = parseFloat(value.replace(/[^0-9.]/g, ''));
                    if (!isNaN(numValue)) {
                        let current = 0;
                        const increment = numValue / 50;
                        const duration = 1500;
                        const stepTime = duration / 50;

                        const updateStat = () => {
                            current += increment;
                            if (current < numValue) {
                                stat.textContent = Math.floor(current) + value.replace(/[0-9.]/g, '');
                                setTimeout(updateStat, stepTime);
                            } else {
                                stat.textContent = value;
                            }
                        };

                        // Start animation when in view
                        const observer = new IntersectionObserver((entries) => {
                            entries.forEach(entry => {
                                if (entry.isIntersecting) {
                                    updateStat();
                                    observer.unobserve(entry.target);
                                }
                            });
                        }, { threshold: 0.5 });

                        observer.observe(stat);
                    }
                }
            });
        }

        animateStats();

        // Impact progress bars animation
        function animateProgressBars() {
            const bars = document.querySelectorAll('.bg-green-300');

            bars.forEach(bar => {
                const width = bar.style.width;
                bar.style.width = '0%';

                setTimeout(() => {
                    bar.style.transition = 'width 1.5s ease-in-out';
                    bar.style.width = width;
                }, 500);
            });
        }

        // Trigger progress bar animation when in view
        const progressObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateProgressBars();
                    progressObserver.unobserve(entry.target);
                }
            });
        }, { threshold: 0.5 });

        const impactSection = document.querySelector('.bg-white\\/10');
        if (impactSection) {
            progressObserver.observe(impactSection);
        }

        // Interactive demo elements
        const demoButtons = document.querySelectorAll('.p-3 button');
        demoButtons.forEach(button => {
            button.addEventListener('click', function() {
                const card = this.closest('.p-3');
                card.style.backgroundColor = '#f0f9ff';

                // Show success message
                const originalText = this.textContent;
                this.textContent = 'Added!';
                this.classList.remove('bg-orange-500', 'bg-green-500');
                this.classList.add('bg-blue-500');

                setTimeout(() => {
                    this.textContent = originalText;
                    this.classList.remove('bg-blue-500');
                    this.classList.add(originalText.includes('Use First') ? 'bg-orange-500' : 'bg-green-500');
                    card.style.backgroundColor = '';
                }, 1500);
            });
        });

        // Recipe card hover in demo
        const demoCards = document.querySelectorAll('.p-3.border');
        demoCards.forEach(card => {
            card.addEventListener('mouseenter', function() {
                this.style.transform = 'translateX(5px)';
                this.style.transition = 'transform 0.3s ease';
            });

            card.addEventListener('mouseleave', function() {
                this.style.transform = 'translateX(0)';
            });

            card.addEventListener('click', function() {
                const recipeName = this.querySelector('.font-medium').textContent;
                showNotification(`Selected: ${recipeName}`, 'info');
            });
        });

        // CTA button interactions
        const ctaButtons = document.querySelectorAll('button:contains("Start Free Trial"), button:contains("Watch 3-Min Demo")');
        ctaButtons.forEach(button => {
            button.addEventListener('click', function() {
                const action = this.textContent.includes('Trial') ? 'trial' : 'demo';

                // Animation
                this.style.transform = 'scale(0.95)';
                setTimeout(() => {
                    this.style.transform = 'scale(1)';
                }, 200);

                if (action === 'trial') {
                    showNotification('Starting your 14-day free trial...', 'success');
                    // In real app: open signup modal
                } else {
                    showNotification('Opening demo video...', 'info');
                    // In real app: open video modal
                }
            });
        });

        // Notification function
        function showNotification(message, type) {
            const notification = document.createElement('div');
            notification.className = `fixed top-6 right-6 px-6 py-4 rounded-xl shadow-xl z-50 transform transition-all duration-500 animate-slide-in-right ${
                type === 'success'
                    ? 'bg-green-500 text-white'
                    : 'bg-orange-500 text-white'
            }`;
            notification.innerHTML = `
                    <div class="flex items-center">
                        <i class="fas fa-${type === 'success' ? 'check-circle' : 'info-circle'} mr-3 text-xl"></i>
                        <span>${message}</span>
                        <button onclick="this.parentElement.parentElement.remove()" class="ml-4">
                            <i class="fas fa-times"></i>
                        </button>
                    </div>
                `;

            document.body.appendChild(notification);

            setTimeout(() => {
                if (notification.parentElement) {
                    notification.remove();
                }
            }, 3000);
        }

        // Animate sections on scroll
        const sectionObserver = new IntersectionObserver((entries) => {
            entries.forEach((entry, index) => {
                if (entry.isIntersecting) {
                    setTimeout(() => {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }, index * 200);
                }
            });
        }, { threshold: 0.1 });

        // Observe sections
        document.querySelectorAll('section > .container > div').forEach((section, index) => {
            section.style.opacity = '0';
            section.style.transform = 'translateY(30px)';
            section.style.transition = 'opacity 0.8s ease, transform 0.8s ease';
            section.style.transitionDelay = `${index * 0.1}s`;
            sectionObserver.observe(section);
        });
    });
</script>
