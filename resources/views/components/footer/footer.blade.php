<!-- JavaScript for Footer Interactions -->
<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Set current year in copyright
        document.getElementById('current-year').textContent = new Date().getFullYear();
        
        // Back to top button
        const backToTopButton = document.getElementById('back-to-top');
        
        window.addEventListener('scroll', function() {
            if (window.scrollY > 300) {
                backToTopButton.classList.remove('opacity-0', 'invisible');
                backToTopButton.classList.add('opacity-100', 'visible');
            } else {
                backToTopButton.classList.remove('opacity-100', 'visible');
                backToTopButton.classList.add('opacity-0', 'invisible');
            }
        });
        
        backToTopButton.addEventListener('click', function() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
        
        // Newsletter form submission
        const newsletterForm = document.querySelector('form');
        if (newsletterForm) {
            newsletterForm.addEventListener('submit', function(e) {
                e.preventDefault();
                const email = this.querySelector('input[type="email"]').value;
                
                // Show success message
                const button = this.querySelector('button[type="submit"]');
                const originalText = button.textContent;
                
                button.innerHTML = '<i class="fas fa-check mr-2"></i> Subscribed!';
                button.classList.remove('from-orange-500', 'to-yellow-500');
                button.classList.add('from-green-500', 'to-green-600');
                
                setTimeout(() => {
                    button.textContent = originalText;
                    button.classList.remove('from-green-500', 'to-green-600');
                    button.classList.add('from-orange-500', 'to-yellow-500');
                    this.querySelector('input[type="email"]').value = '';
                }, 2000);
                
                // In a real app, you would send this to your backend
                console.log('Subscribed email:', email);
            });
        }
        
        // Animate achievement badges on scroll
        const observerOptions = {
            threshold: 0.5,
            rootMargin: '0px 0px -50px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('animate-pulse');
                    setTimeout(() => {
                        entry.target.classList.remove('animate-pulse');
                    }, 1000);
                }
            });
        }, observerOptions);
        
        // Observe achievement badges
        document.querySelectorAll('.bg-gray-800\\/50').forEach(badge => {
            observer.observe(badge);
        });
    });
</script>

